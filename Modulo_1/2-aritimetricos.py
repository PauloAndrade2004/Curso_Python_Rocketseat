#Aritimetrico:

# + Adição
# - Subtração
# * Multiplicação
# / Divisão
# // Divisão inteira
# ** Potenciação
# % Resto da divisão


adicao_numero = 10 + 10
print(adicao_numero)

subtracao_numero = 10 - 5
print(subtracao_numero)

multiplicacao_numero = 10 * 5
print(multiplicacao_numero)

divisao_numero = 10 / 2
print(divisao_numero)

divisao_inteira_numero = 10 // 3
print(divisao_inteira_numero)

potenciacao_numero = 10 ** 2
print(potenciacao_numero)

resto_divisao_numero = 10 % 3
print(resto_divisao_numero)

